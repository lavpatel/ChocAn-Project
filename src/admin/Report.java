    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Session session = Utils.getSession();
        session.beginTransaction();
        
        DefaultTableModel model = new DefaultTableModel(new String[]{"Service ID","Service Name","Provider ID","Provider Name","Fees","Date","Comments"},0);
        
        List<ServiceProvided> services = session.createCriteria(ServiceProvided.class)
                .add(Restrictions.eq("providerNumber", Long.parseLong(providerid.getText())))
                .list();
        
        services.forEach(e -> model.addRow(new Object[]{e.getServiceId(),e.getServiceName(),e.getProviderNumber(),e.getProviderName(),e.getFees(),e.getTimedate().toString(),e.getComments()}));
        jTable1.setModel(model);
        
        session.getTransaction().commit();
        session.close();
    }//GEN-LAST:event_jButton1ActionPerformed

