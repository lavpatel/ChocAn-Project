    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Session session = Utils.getSession();
        session.beginTransaction();
        Members member = new Members();
        member.setId(member.generateId());
        member.setName(memname.getText());
        member.setAddress(memadd.getText());
        member.setCity(memcity.getText());
        member.setState(memstate.getText());
        member.setZipCode(mempostal.getText());
        member.setIsValid(false);
        
        session.save(member);
        
        session.getTransaction().commit();
        session.close();
        
        JOptionPane.showMessageDialog(this, "Added Successfully");
        
        memname.setText("");
        memadd.setText("");
        memcity.setText("");
        memstate.setText("");
        mempostal.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

